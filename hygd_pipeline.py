# 4. MODEL — EfficientNet-B3 dengan Fine-tuning
# ─────────────────────────────────────────────
def build_model(num_classes=2, freeze_backbone=False):
    """
    EfficientNet-B3 pretrained ImageNet.
    Lebih ringan dari ResNet tapi akurasi lebih tinggi untuk medical imaging.
    """
    model = models.efficientnet_b3(weights=models.EfficientNet_B3_Weights.IMAGENET1K_V1)

    if freeze_backbone:
        for param in model.parameters():
            param.requires_grad = False

    # Ganti classifier head
    in_features = model.classifier[1].in_features
    model.classifier = nn.Sequential(
        nn.Dropout(p=0.4),
        nn.Linear(in_features, 256),
        nn.ReLU(),
        nn.Dropout(p=0.3),
        nn.Linear(256, num_classes)
    )
    return model

