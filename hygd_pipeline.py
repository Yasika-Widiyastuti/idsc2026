def compute_class_weights(train_df):
    """Handle class imbalance dengan class weights"""
    counts = train_df["label_int"].value_counts().sort_index()
    total = len(train_df)
    weights = torch.tensor(
        [total / (2 * counts[i]) for i in range(2)],
        dtype=torch.float
    ).to(DEVICE)
    print(f"\nClass weights: GON-={weights[0]:.3f}, GON+={weights[1]:.3f}")
    return weights


def train_one_epoch(model, loader, criterion, optimizer):
    model.train()
    total_loss, correct, total = 0, 0, 0
    for imgs, labels in loader:
        imgs, labels = imgs.to(DEVICE), labels.to(DEVICE)
        optimizer.zero_grad()
        outputs = model(imgs)
        loss = criterion(outputs, labels)
        loss.backward()
        optimizer.step()
        total_loss += loss.item() * imgs.size(0)
        preds = outputs.argmax(dim=1)
        correct += (preds == labels).sum().item()
        total += imgs.size(0)
    return total_loss / total, correct / total


@torch.no_grad()
def evaluate(model, loader, criterion):
    model.eval()
    total_loss, correct, total = 0, 0, 0
    all_probs, all_labels = [], []
    for imgs, labels in loader:
        imgs, labels = imgs.to(DEVICE), labels.to(DEVICE)
        outputs = model(imgs)
        loss = criterion(outputs, labels)
        total_loss += loss.item() * imgs.size(0)
        probs = torch.softmax(outputs, dim=1)[:, 1]
        preds = outputs.argmax(dim=1)
        correct += (preds == labels).sum().item()
        total += imgs.size(0)
        all_probs.extend(probs.cpu().numpy())
        all_labels.extend(labels.cpu().numpy())
    auc = roc_auc_score(all_labels, all_probs)
    return total_loss / total, correct / total, auc, all_probs, all_labels


def train_model(model, train_loader, val_loader, class_weights, epochs=30, lr=1e-4):
    criterion = nn.CrossEntropyLoss(weight=class_weights)
    optimizer = optim.AdamW(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = CosineAnnealingLR(optimizer, T_max=epochs, eta_min=1e-6)

    best_auc = 0
    best_model_path = os.path.join(OUTPUT_DIR, "best_model.pth")
    history = {"train_loss": [], "val_loss": [], "train_acc": [], "val_acc": [], "val_auc": []}

    print("\n━━━ TRAINING ━━━")
    for epoch in range(1, epochs + 1):
        tr_loss, tr_acc = train_one_epoch(model, train_loader, criterion, optimizer)
        vl_loss, vl_acc, vl_auc, _, _ = evaluate(model, val_loader, criterion)
        scheduler.step()

        history["train_loss"].append(tr_loss)
        history["val_loss"].append(vl_loss)
        history["train_acc"].append(tr_acc)
        history["val_acc"].append(vl_acc)
        history["val_auc"].append(vl_auc)

        if vl_auc > best_auc:
            best_auc = vl_auc
            torch.save(model.state_dict(), best_model_path)
            flag = " ← BEST"
        else:
            flag = ""

        print(f"Epoch {epoch:02d}/{epochs} | "
              f"Loss: {tr_loss:.4f}/{vl_loss:.4f} | "
              f"Acc: {tr_acc:.3f}/{vl_acc:.3f} | "
              f"Val AUC: {vl_auc:.4f}{flag}")

    print(f"\n✓ Best Val AUC: {best_auc:.4f} → disimpan ke {best_model_path}")
    return history, best_model_path


