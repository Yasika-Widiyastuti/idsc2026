def final_evaluation(model, test_loader, class_weights):
    criterion = nn.CrossEntropyLoss(weight=class_weights)
    _, acc, auc, all_probs, all_labels = evaluate(model, test_loader, criterion)
    preds = [1 if p >= 0.5 else 0 for p in all_probs]

    print("\n━━━ FINAL TEST RESULTS ━━━")
    print(f"Accuracy : {acc:.4f}")
    print(f"AUC-ROC  : {auc:.4f}")
    print("\nClassification Report:")
    print(classification_report(all_labels, preds, target_names=["GON-", "GON+"]))

    # Confusion Matrix
    cm = confusion_matrix(all_labels, preds)
    disp = ConfusionMatrixDisplay(cm, display_labels=["GON-", "GON+"])
    fig, ax = plt.subplots(figsize=(6, 5))
    disp.plot(ax=ax, colorbar=False, cmap="Blues")
    ax.set_title(f"Confusion Matrix (Test AUC={auc:.4f})")
    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, "confusion_matrix.png"), dpi=150)
    plt.close()

    # ROC Curve
    fpr, tpr, _ = roc_curve(all_labels, all_probs)
    plt.figure(figsize=(6, 5))
    plt.plot(fpr, tpr, color="#e63946", lw=2, label=f"AUC = {auc:.4f}")
    plt.plot([0, 1], [0, 1], "k--", lw=1)
    plt.xlabel("FPR"); plt.ylabel("TPR")
    plt.title("ROC Curve — Test Set")
    plt.legend(); plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, "roc_curve.png"), dpi=150)
    plt.close()
    print("Plots disimpan ke outputs/")

    return auc

