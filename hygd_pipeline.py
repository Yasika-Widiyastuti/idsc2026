# 8. MAIN PIPELINE
# ─────────────────────────────────────────────
def main():
    print("=" * 60)
    print("  HYGD Glaucoma Pipeline — IDSC 2026")
    print("=" * 60)

    # Step 1: Load & EDA
    df = load_and_eda(LABELS_CSV, min_quality=MIN_QUALITY)

    # Step 2: Patient-level split
    train_df, val_df, test_df = patient_level_split(df, seed=SEED)

    # Step 3: Transforms & Datasets
    train_tf, val_tf = get_transforms(IMG_SIZE)
    train_ds = HYGDDataset(train_df, IMAGES_DIR, train_tf)
    val_ds   = HYGDDataset(val_df, IMAGES_DIR, val_tf)
    test_ds  = HYGDDataset(test_df, IMAGES_DIR, val_tf)

    train_loader = DataLoader(train_ds, batch_size=BATCH_SIZE, shuffle=True,  num_workers=2, pin_memory=True)
    val_loader   = DataLoader(val_ds,   batch_size=BATCH_SIZE, shuffle=False, num_workers=2, pin_memory=True)
    test_loader  = DataLoader(test_ds,  batch_size=BATCH_SIZE, shuffle=False, num_workers=2, pin_memory=True)

    # Step 4: Model
    model = build_model(num_classes=2).to(DEVICE)
    print(f"\nModel: EfficientNet-B3")
    total_params = sum(p.numel() for p in model.parameters())
    trainable   = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"Total params: {total_params:,} | Trainable: {trainable:,}")

    # Step 5: Class weights
    class_weights = compute_class_weights(train_df)

    # Step 6: Training (Phase 1 — full fine-tune)
    history, best_path = train_model(model, train_loader, val_loader, class_weights, EPOCHS, LR)
    plot_training_history(history)

    # Step 7: Load best model & evaluate
    model.load_state_dict(torch.load(best_path, map_location=DEVICE))
    final_auc = final_evaluation(model, test_loader, class_weights)

    # Step 8: GradCAM
    print("\n━━━ GENERATING GRADCAM ━━━")
    visualize_gradcam(model, test_ds, n_samples=6)

    print("\n" + "=" * 60)
    print(f"  PIPELINE SELESAI! Final Test AUC: {final_auc:.4f}")
    print(f"  Semua output tersimpan di folder: {OUTPUT_DIR}/")
    print("=" * 60)
    print("\nFile outputs:")
    print("  ├── best_model.pth        → model terbaik")
    print("  ├── eda_plots.png         → eksplorasi data")
    print("  ├── training_history.png  → kurva training")
    print("  ├── confusion_matrix.png  → evaluasi test")
    print("  ├── roc_curve.png         → ROC & AUC")
    print("  └── gradcam_results.png   → interpretability")


if __name__ == "__main__":
    main()

