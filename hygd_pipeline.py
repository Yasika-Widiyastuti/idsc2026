# 7. GRADCAM — Interpretability (WAJIB untuk kompetisi!)
# ─────────────────────────────────────────────
class GradCAM:
    """
    Grad-CAM untuk EfficientNet.
    Menghasilkan heatmap yang menunjukkan AREA MANA yang dipakai model
    untuk memutuskan glaukoma — ini yang bikin juri terkesan.
    """
    def __init__(self, model, target_layer):
        self.model = model
        self.gradients = None
        self.activations = None
        target_layer.register_forward_hook(self._save_activation)
        target_layer.register_backward_hook(self._save_gradient)

    def _save_activation(self, module, input, output):
        self.activations = output.detach()

    def _save_gradient(self, module, grad_input, grad_output):
        self.gradients = grad_output[0].detach()

    def generate(self, img_tensor, class_idx=None):
        self.model.eval()
        img_tensor = img_tensor.unsqueeze(0).to(DEVICE)
        output = self.model(img_tensor)

        if class_idx is None:
            class_idx = output.argmax(dim=1).item()

        self.model.zero_grad()
        output[0, class_idx].backward()

        weights = self.gradients.mean(dim=(2, 3), keepdim=True)
        cam = (weights * self.activations).sum(dim=1, keepdim=True)
        cam = torch.relu(cam).squeeze().cpu().numpy()
        cam = (cam - cam.min()) / (cam.max() - cam.min() + 1e-8)
        return cam, class_idx


def visualize_gradcam(model, dataset, n_samples=6):
    """Simpan GradCAM untuk n sampel dari test set"""
    # Target layer terakhir EfficientNet features
    target_layer = model.features[-1]
    gradcam = GradCAM(model, target_layer)

    val_tf = get_transforms(IMG_SIZE)[1]
    indices = random.sample(range(len(dataset)), min(n_samples, len(dataset)))

    fig, axes = plt.subplots(n_samples, 3, figsize=(12, 4 * n_samples))
    if n_samples == 1:
        axes = [axes]

    label_names = {0: "GON- (Normal)", 1: "GON+ (Glaucoma)"}

    for i, idx in enumerate(indices):
        img_tensor, true_label = dataset[idx]

        # Original image (denormalize)
        mean = torch.tensor([0.485, 0.456, 0.406]).view(3, 1, 1)
        std  = torch.tensor([0.229, 0.224, 0.225]).view(3, 1, 1)
        img_denorm = (img_tensor * std + mean).clamp(0, 1).permute(1, 2, 0).numpy()

        # GradCAM
        cam, pred_idx = gradcam.generate(img_tensor)
        cam_resized = np.array(Image.fromarray(
            (cam * 255).astype(np.uint8)
        ).resize((IMG_SIZE, IMG_SIZE))) / 255.0

        # Overlay
        heatmap = cm.jet(cam_resized)[..., :3]
        overlay = (0.5 * img_denorm + 0.5 * heatmap).clip(0, 1)

        axes[i][0].imshow(img_denorm); axes[i][0].set_title(f"Original\nTrue: {label_names[true_label.item()]}")
        axes[i][1].imshow(cam_resized, cmap="jet"); axes[i][1].set_title("GradCAM Heatmap")
        axes[i][2].imshow(overlay); axes[i][2].set_title(f"Overlay\nPred: {label_names[pred_idx]}")
        for ax in axes[i]: ax.axis("off")

    plt.suptitle("GradCAM — Model Interpretability", fontsize=14, fontweight="bold")
    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, "gradcam_results.png"), dpi=150)
    plt.close()
    print("GradCAM disimpan ke outputs/gradcam_results.png")

