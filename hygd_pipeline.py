# 2. PATIENT-LEVEL SPLIT (anti data leakage!)
# ─────────────────────────────────────────────
def patient_level_split(df, test_size=0.15, val_size=0.15, seed=42):
    """
    Split berdasarkan Patient ID — BUKAN per gambar.
    Ini krusial karena 1 pasien bisa punya banyak gambar.
    Kalau split per gambar → data leakage → hasil ngeprank.
    """
    patients = df["Patient"].unique()

    # Stratified split berdasarkan majority label per patient
    patient_labels = df.groupby("Patient")["label_int"].agg(
        lambda x: int(x.mean() >= 0.5)
    ).reset_index()
    patient_labels.columns = ["Patient", "patient_label"]

    p_train_val, p_test = train_test_split(
        patient_labels["Patient"],
        test_size=test_size,
        stratify=patient_labels["patient_label"],
        random_state=seed
    )
    val_ratio = val_size / (1 - test_size)
    p_train, p_val = train_test_split(
        p_train_val,
        test_size=val_ratio,
        stratify=patient_labels[patient_labels["Patient"].isin(p_train_val)]["patient_label"],
        random_state=seed
    )

    train_df = df[df["Patient"].isin(p_train)].reset_index(drop=True)
    val_df   = df[df["Patient"].isin(p_val)].reset_index(drop=True)
    test_df  = df[df["Patient"].isin(p_test)].reset_index(drop=True)

    print(f"\n━━━ PATIENT-LEVEL SPLIT ━━━")
    print(f"Train: {len(train_df)} images ({train_df['Patient'].nunique()} patients)")
    print(f"Val  : {len(val_df)} images ({val_df['Patient'].nunique()} patients)")
    print(f"Test : {len(test_df)} images ({test_df['Patient'].nunique()} patients)")

    # Cek tidak ada overlap
    assert len(set(p_train) & set(p_val)) == 0, "OVERLAP TRAIN-VAL!"
    assert len(set(p_train) & set(p_test)) == 0, "OVERLAP TRAIN-TEST!"
    print("✓ Tidak ada patient overlap antar split")

    return train_df, val_df, test_df

